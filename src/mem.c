#include "mem.h"
#include <string.h>

// Built-in font data (0-F)
static const uint8_t fontset[80] = {
    0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
    0x20, 0x60, 0x20, 0x20, 0x70, // 1
    0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
    0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
    0x90, 0x90, 0xF0, 0x10, 0x10, // 4
    0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
    0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
    0xF0, 0x10, 0x20, 0x40, 0x40, // 7
    0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
    0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
    0xF0, 0x90, 0xF0, 0x90, 0x90, // A
    0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
    0xF0, 0x80, 0x80, 0x80, 0xF0, // C
    0xE0, 0x90, 0x90, 0x90, 0xE0, // D
    0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
    0xF0, 0x80, 0xF0, 0x80, 0x80  // F
};

void memory_init(Memory* mem) {
    memory_reset(mem);
    // Load fontset into memory
    memcpy(&mem->memory[FONT_START_ADDR], fontset, sizeof(fontset));
}

void memory_reset(Memory* mem) {
    memset(mem->memory, 0, MEMORY_SIZE);
}

uint8_t memory_read(Memory* mem, uint16_t address) {
    if (address >= MEMORY_SIZE) {
        return 0;
    }
    return mem->memory[address];
}

void memory_write(Memory* mem, uint16_t address, uint8_t value) {
    if (address < MEMORY_SIZE) {
        mem->memory[address] = value;
    }
}

bool memory_load_rom(Memory* mem, const uint8_t* data, size_t size) {
    if (size > (MEMORY_SIZE - PROGRAM_START_ADDRESS)) {
        return false;
    }
    
    memcpy(&mem->memory[PROGRAM_START_ADDRESS], data, size);
    return true;
}
